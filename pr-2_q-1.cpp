#include <iostream>
using namespace std;

int main()
{
    int n1, n2;
    cout << "Enter two numbers : ";
    cin >> n1 >> n2;
    cout << "Addition : " << n1 + n2 << endl;
    cout << "Subtraction : " << n1 - n2 << endl;
    cout << "Multiplication : " << n1 * n2 << endl;
    cout << "Division : " << n1 / n2 << endl;
    cout << "Modulo : " << n1 % n2;
    return 0;
}